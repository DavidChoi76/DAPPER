    @dataclass
    class Replacement:
        lines: list
        nOpen: int
        nClose: int

    def backtrack_until_finding(substr, lineno):
        while True:
            lineno -= 1
            if substr in orig_code[lineno]:
                return lineno

    replacements = []
    orig_code = open(__file__, "r").readlines()

